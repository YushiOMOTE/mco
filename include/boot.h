#ifndef __BOOT_H
#define __BOOT_H

/* CR4: DE,PSE,PAE,PGE,PCE,OSFXSR,OSXMMEXCPT
 * CR0: PE,WP,PG
 * EFER: EMB
 */
#define BOOT_CR4 0x000007b8
#define BOOT_CR0 0x80010001
#define BOOT_EFER 0x00000900

#define BOOT_E820_BASE 0xe000
#define BOOT_E820_SIZE 0xdff4

#define BOOT_PAGE_TBL_BASE 0xf000
#define BOOT_PAGE_TBL_SIZE 0x2000

#define BOOT_STACK_SIZE   0x4000000 /* 64 MiB */
#define BOOT_HEAP_BASE    0x100000  /* 1 MiB */
#define BOOT_BSP_HEAP_BASE (BOOT_HEAP_BASE + BOOT_STACK_SIZE)
#define BOOT_BSP_STACK_BASE (BOOT_HEAP_BASE + BOOT_STACK_SIZE)

#define BOOT_STACK16_BASE 0x7c00

#endif	/* __BOOT_H */
